-------- LCR REPORT SECTION
--wyswietla jakie zostaly utworzone raporty na dana SD i dla pewnych REPORT_NAME
--LCR_R1
use DB_NAME; \
select DISTINCT REPORT_NAME, PORTFOLIO_NAME, SCENARIO_NAME, BASEL_REPORTING_FRAMEWORK_ENUM \
  from LCR_REPORTS \
 WHERE ('AAAA_PAR' = '' AND SITUATION_DATE = (select max(SITUATION_DATE) from BSDATE) \
        OR SITUATION_DATE = 'AAAA_PAR') \
   AND REPORT_NAME like '%BBBB_PAR%' \
 ORDER BY REPORT_NAME, PORTFOLIO_NAME, SCENARIO_NAME, BASEL_REPORTING_FRAMEWORK_ENUM;

--wyswietla jakie zostaly utworzone audyty na dana SD i dla pewnych REPORT_NAME
